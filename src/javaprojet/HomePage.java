    //bouton pdf de virement
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int selectedRow = jTableVirement.getSelectedRow(); // Obtenez la ligne sélectionnée

        // Vérifiez si une ligne est sélectionnée
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne dans le tableau.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Récupérer les données depuis le tableau sélectionné
        String numCompte = (String) jTableVirement.getValueAt(selectedRow, 0);
        String nomEnvoyeur = (String) jTableVirement.getValueAt(selectedRow, 1);
        String prenomsEnvoyeur = (String) jTableVirement.getValueAt(selectedRow, 2);
        String numCompteB = (String) jTableVirement.getValueAt(selectedRow, 3);
        String nomBeneficiaire = (String) jTableVirement.getValueAt(selectedRow, 4);
        String prenomsBeneficiaire = (String) jTableVirement.getValueAt(selectedRow, 5);
        String montantStr = (String) jTableVirement.getValueAt(selectedRow, 6);
        String dateTransfertStr = (String) jTableVirement.getValueAt(selectedRow, 7);

        // Convertir montant en Double
        Double montant = Double.parseDouble(montantStr);

        // Convertir la chaîne de caractères de date en objet java.util.Date
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss.SSS");
        Date utilDateTransfert = null;
        try {
            utilDateTransfert = (Date) sdf.parse(dateTransfertStr);
        } catch (java.text.ParseException ex) {
            Logger.getLogger(HomePage.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Convertir java.util.Date en java.sql.Timestamp
        Timestamp sqlDateTransfert = new Timestamp(utilDateTransfert.getTime());

        DBConnect dbConnect = new DBConnect();
        dbConnect.dbConnection();

        ClientModel clientModel = new ClientModel(dbConnect);

        // Récupérer le solde de l'envoyeur
        Double soldeEnvoyeur = clientModel.getClientSolde(dbConnect, numCompte);

        // Vérifiez que le solde n'est pas nul
        if (soldeEnvoyeur == null) {
            JOptionPane.showMessageDialog(this, "Erreur de récupération du solde. Veuillez vérifier les données.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Appeler la méthode pour générer le PDF de virement
        clientModel.genererVirement(sqlDateTransfert, numCompte, "005", nomEnvoyeur, prenomsEnvoyeur, soldeEnvoyeur, numCompteB, nomBeneficiaire, prenomsBeneficiaire, montant);

        // Afficher une boîte de dialogue confirmant que le PDF a été généré
        JOptionPane.showMessageDialog(this, "Le PDF du virement a été généré avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton8ActionPerformed

    //bouton mail de prêt
    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        int selectedRow = jTablePret.getSelectedRow(); // Obtenez la ligne sélectionnée

        // Vérifiez si une ligne est sélectionnée
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne dans le tableau.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Récupérer les données depuis le tableau sélectionné
        String numCompte = (String) jTablePret.getValueAt(selectedRow, 1);
        Object montantPreteObj = jTablePret.getValueAt(selectedRow, 4);
        Object datePretObj = jTablePret.getValueAt(selectedRow, 5);

        double montant_Prete = 0.0;
        try {
            // Convertir l'objet en String
            String montantPreteStr = (montantPreteObj != null) ? montantPreteObj.toString() : "";

            // Convertir la String en Double
            montant_Prete = Double.parseDouble(montantPreteStr);

            // Vérifier si le montant est valide (non négatif)
            if (montant_Prete <= 0.0) {
                throw new NumberFormatException("Montant de prêt invalide : " + montantPreteStr);
            }

            DBConnect dbConnect = new DBConnect();
            dbConnect.dbConnection();

            ClientModel clientModel = new ClientModel(dbConnect);

            try {
                // Récupérer les informations du client pour le mail
                String[] clientInfo = clientModel.getClientInfo(dbConnect, numCompte);
                if (clientInfo == null || clientInfo.length < 3) {
                    System.out.println("Informations du client non trouvées pour le compte : " + numCompte);
                    return;
                }

                String nom = clientInfo[0];
                String prenoms = clientInfo[1];
                String mail = clientInfo[2];

                // Convertir la valeur de la date en Timestamp
                Timestamp datePret = null;
                if (datePretObj instanceof Timestamp) {
                    datePret = (Timestamp) datePretObj;
                } else if (datePretObj instanceof String) {
                    try {
                        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                        Date parsedDate = dateFormat.parse((String) datePretObj);
                        datePret = new Timestamp(parsedDate.getTime());
                    } catch (java.text.ParseException ex) {
                        Logger.getLogger(HomePage.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    System.out.println("Type de date non pris en charge : " + datePretObj.getClass().getName());
                    JOptionPane.showMessageDialog(this, "Type de date non pris en charge : " + datePretObj.getClass().getName(), "Erreur", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // Calculer la date limite de remboursement (200 jours après le prêt)
                Calendar cal = Calendar.getInstance();
                cal.setTime(datePret);
                cal.add(Calendar.DAY_OF_YEAR, 200);
                String dueDate = new SimpleDateFormat("yyyy-MM-dd").format(cal.getTime());

                // Formatter le montant à rembourser
                DecimalFormat df = new DecimalFormat("#.##");
                String montantRembourse = df.format(montant_Prete * 1.10);

                // Envoyer la notification par e-mail
                Mail mailer = new Mail();
                mailer.sendNotification(mail, nom + " " + prenoms, montantRembourse, dueDate);

                // Afficher une boîte de dialogue confirmant l'envoi du mail
                JOptionPane.showMessageDialog(this, "E-mail envoyé avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException | MessagingException | UnsupportedEncodingException ex) {
                Logger.getLogger(HomePage.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Erreur lors de l'envoi du mail : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Montant de prêt invalide : " + montantPreteObj, "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton12ActionPerformed

