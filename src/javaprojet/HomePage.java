    //Ajout de listener dans le champ de recherche
    private void initializeSearchField() {
        jTextFieldRecherche.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                performSearch();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                performSearch();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                performSearch();
            }
        });
    }

    private void performSearch() {
        String searchTerm = jTextFieldRecherche.getText().trim();
        searchClient(searchTerm);
    }

    private void searchClient(String searchTerm) {
        ResultSet rs;
        String[] columns = {"numCompte", "Nom", "Prenoms", "Tel", "Mail", "Solde"};
        DefaultTableModel model = new DefaultTableModel(null, columns);

        String query = "SELECT numCompte, Nom, Prenoms, Tel, Mail, Solde FROM client WHERE numCompte LIKE ? OR Nom LIKE ? OR Prenoms LIKE ?";

        DBConnect dbConnect = new DBConnect();
        dbConnect.dbConnection();

        try (PreparedStatement pstmt = dbConnect.connection.prepareStatement(query)) {
            pstmt.setString(1, "%" + searchTerm + "%");
            pstmt.setString(2, "%" + searchTerm + "%");
            pstmt.setString(3, "%" + searchTerm + "%");
            rs = pstmt.executeQuery();

            while (rs.next()) {
                String[] row = new String[6];
                row[0] = rs.getString("numCompte");
                row[1] = rs.getString("Nom");
                row[2] = rs.getString("Prenoms");
                row[3] = rs.getString("Tel");
                row[4] = rs.getString("Mail");
                row[5] = String.valueOf(rs.getDouble("Solde"));
                model.addRow(row);
            }

            jTableCli.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Erreur lors de la recherche du client: " + ex);
        }
    }

